exports.updateProfile = async (req, res) => {
  try {
    const {
      firstName = "",
      lastName = "",
      dateOfBirth = "",
      about = "",
      contactNumber = "",
      gender = "",
    } = req.body
    const id = req.user.id

    //TODO: Find the profile by id
    const userDetails = await User.findById(id)
    //TODO: from Profile table select profile finding by additionalDetails of user
    const profile = await Profile.findById(userDetails.additionalDetails)

    //TODO: change firstName, lastName of user
    const user = await User.findByIdAndUpdate(id, {
      firstName,
      lastName,
    })
    await user.save() //TODO: commit

    //TODO: Update the profile fields using sql 
    profile.dateOfBirth = dateOfBirth
    profile.about = about
    profile.contactNumber = contactNumber
    profile.gender = gender

    // Save the updated profile
    await profile.save() //TODO: commit

    //TODO: Find the updated user details and add it to additionalDetails table
    const updatedUserDetails = await User.findById(id)
      .populate("additionalDetails")
      .exec()

    return res.json({
      success: true,
      message: "Profile updated successfully",
      updatedUserDetails,
    })
  } catch (error) {
    console.log(error)
    return res.status(500).json({
      success: false,
      error: error.message,
    })
  }
}

exports.deleteAccount = async (req, res) => {
  try {
    const id = req.user.id
    console.log(id)
    //TODO: find user by id
    const user = await User.findById({ _id: id })
    if (!user) {
      return res.status(404).json({
        success: false,
        message: "User not found",
      })
    }
  
    //TODO: Delete Assosiated Profile with the User
    await Profile.findByIdAndDelete({
      _id: new mongoose.Types.ObjectId(user.additionalDetails),
    })
    //From user table's course column (for a specific user, found by his userid)
    for (const courseId of user.courses) {
      //TODO: delete user info from all the courses he enrolled
      await Course.findByIdAndUpdate(
        courseId,
        { $pull: { studentsEnroled: id } },
        { new: true }
      )
    }
    //TODO: Now Delete User by his id
    await User.findByIdAndDelete({ _id: id })
    res.status(200).json({
      success: true,
      message: "User deleted successfully",
    })
    await CourseProgress.deleteMany({ userId: id })
  } catch (error) {
    console.log(error)
    res
      .status(500)
      .json({ success: false, message: "User Cannot be deleted successfully" })
  }
}

