Manage Layout Overrides for your platform. These allow you
to load dynamic CSS and Javascript rules based on conditions
that override existing frontend UI components and behaviors.

The displayed fields include:
* `ID`: The unique record ID.
* `Name`: The title for the credits entry.
* `Route`: The body of the credits entry.
* `Guest`: Whether or not the override will appear for guest users.
* `Repeating`: Whether or not the override repeats.
* `Player ID`: An optional player that this override will appear.
* `Valid From`: Date the override will start to take effect.
* `Valid Until`: Date the override will finish taking effect.