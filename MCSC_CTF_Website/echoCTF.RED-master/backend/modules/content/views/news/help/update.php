Update an existing news item to be displayed on <a href="//<?=Yii::$app->sys->offense_domain?>/dashboard" title="Frontend Dashboard page" target="_blank">frontend dashboard page</a>

The form includes the following fields:
* `Title`: The title of the news item
* `Category`: A category for the entry. It supports HTML so you can use images such as `<img src="/images/news/category/target-migration.svg" width="25px" alt="">`
* `Body`: The content for the new item (supports HTML).
