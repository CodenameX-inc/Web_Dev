Create a new Objective entry for your platform. These objectives
appear to the players as a way to help them understand
what they need to achieve on this platform and/or competition.

The form fields include:
* `Title`: The title of the objective (supports HTML).
* `Player Type`: The player type this objective will appear.
* `Message`: The content of the objective (supports HTML).
* `Weight`: The weight of the entry used for ordering.